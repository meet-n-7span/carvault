function normalizeSearchValue(value) {
  return String(value ?? "")
    .trim()
    .toLowerCase();
}

function includesSelection(selectedValues, value) {
  return selectedValues.length === 0 || selectedValues.includes(value);
}

function matchesSearch(car, searchValue) {
  const normalizedSearch = normalizeSearchValue(searchValue);

  if (!normalizedSearch) {
    return true;
  }

  const searchableFields = [car.brand, car.model, car.variant];

  return searchableFields.some((field) =>
    String(field).toLowerCase().includes(normalizedSearch),
  );
}

function sortCars(cars, sortBy) {
  const sortableCars = [...cars];

  switch (sortBy) {
    case "Price Low to High":
      return sortableCars.sort(
        (firstCar, secondCar) =>
          firstCar.discountedPrice - secondCar.discountedPrice,
      );
    case "Price High to Low":
      return sortableCars.sort(
        (firstCar, secondCar) =>
          secondCar.discountedPrice - firstCar.discountedPrice,
      );
    case "Newest First":
      return sortableCars.sort(
        (firstCar, secondCar) =>
          secondCar.registrationYear - firstCar.registrationYear,
      );
    case "Oldest First":
      return sortableCars.sort(
        (firstCar, secondCar) =>
          firstCar.registrationYear - secondCar.registrationYear,
      );
    case "KM Low to High":
      return sortableCars.sort(
        (firstCar, secondCar) => firstCar.kmDriven - secondCar.kmDriven,
      );
    case "All":
    default:
      return sortableCars;
  }
}

function mapCarToListingCard(car) {
  return {
    id: car.id,
    primaryImage: car.images.primary,
    brand: car.brand,
    model: car.model,
    kmDriven: car.kmDriven,
    fuelType: car.fuelType,
    transmission: car.transmission,
    city: car.location.city,
    state: car.location.state,
    originalPrice: car.originalPrice,
    discountedPrice: car.discountedPrice,
    badge: car.badge,
    slug: car.slug,
  };
}

function buildCarsListingResponse({
  searchValue = "",
  priceValue = [0, Number.MAX_SAFE_INTEGER],
  yearValue = [0, Number.MAX_SAFE_INTEGER],
  kmValue = [0, Number.MAX_SAFE_INTEGER],
  selectedBrands = [],
  selectedFuelTypes = [],
  selectedTransmissions = [],
  selectedBodyTypes = [],
  selectedOwnerships = [],
  sortBy = "All",
} = {}) {
  const filteredCars = carsData.filter((car) => {
    const matchesPrice =
      car.discountedPrice >= priceValue[0] &&
      car.discountedPrice <= priceValue[1];
    const matchesYear =
      car.registrationYear >= yearValue[0] &&
      car.registrationYear <= yearValue[1];
    const matchesKm = car.kmDriven >= kmValue[0] && car.kmDriven <= kmValue[1];

    return (
      matchesSearch(car, searchValue) &&
      matchesPrice &&
      matchesYear &&
      matchesKm &&
      includesSelection(selectedBrands, car.brand) &&
      includesSelection(selectedFuelTypes, car.fuelType) &&
      includesSelection(selectedTransmissions, car.transmission) &&
      includesSelection(selectedBodyTypes, car.bodyType) &&
      includesSelection(selectedOwnerships, car.ownership)
    );
  });

  const sortedCars = sortCars(filteredCars, sortBy);
  const listingCars = sortedCars.map(mapCarToListingCard);

  return {
    total: listingCars.length,
    cars: listingCars,
  };
}

export function getCarsListing(filters = {}) {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve(buildCarsListingResponse(filters));
    }, 500);
  });
}

export function getCarBySlug(slug) {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve(carsData.find((car) => car.slug === slug) ?? null);
    }, 500);
  });
}
